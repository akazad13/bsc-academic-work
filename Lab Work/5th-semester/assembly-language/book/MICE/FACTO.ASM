.MODEL SMALL
.STACK 100H
.DATA

ILLI_MSG DB 0AH,0DH,'ILLIGAL $'
OVERFLOW DB 0AH,0DH,'OVERFLOW $'

.CODE

MAIN PROC
    
    MOV AX,@DATA
    MOV DS,AX
    
    CALL INDEC
    
    PUSH AX
    
    MOV AH,2
    MOV DL,0AH
    INT 21H
    MOV DL,0DH
    INT 21H
    
    POP AX
    MOV CX,AX
    MOV AX,1
    
    TOP:
      MUL CX
      LOOP TOP
      
    CALL OUTDEC
    
@END:
MOV AH,4CH
INT 21H
    
MAIN ENDP

INCLUDE INDEC.ASM
INCLUDE OUTDEC.ASM

END MAIN